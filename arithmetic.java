public class arithmetic{
    public static void main(String[]args){
        // binary operations
        System.out.println(10 % 3);
        System.out.println(10 + 3);
        System.out.println(10 - 3);
        System.out.println(10 / 3);
        // Unary operations
        System.out.println(+5);
    }
}